class Relationaloperators {
    public static void main (String args[])
    {
      int a = 9;
      int b = 6;
      int c=9;

      boolean r = a>b;
      boolean k= a<b;
      boolean x= a==c;
      boolean y= a<=c;
      boolean i= a!=b;
      
      System.out.println(r);
      System.out.println(k);
      System.out.println(x);
      System.out.println(y);
      System.out.println(i);
    }
}